<h1>How to play</h1>

<h4>1. The black bottom bar of your screen is for navigating between pages, and the game board.</h4>
<ul>
	<li>The far left button, a door, is for leaving this game.</li>
	<li>The book button is for viewing this page, which is to help players.</li>
	<li>
		The hour glass button is for the history page, showing all the events that have happened in the
		game. It records when players join, leave, and when they play cards.
	</li>
	<li>
		The far right button, of the two people, is for the game board, where you play UNO. You'll spend
		most of your time there.
	</li>
</ul>

<h4>2. The game board is split into 2 main parts:</h4>
<ul>
	<li>
		The table, on the left, where you can see everyone else's hands, and the two stacks, the discard
		and draw pile.
	</li>
	<li>
		Your hand is on the right, in a vertical strip. You can't reorder the cards in your hand, but
		they should automagically end up in a pleasing order.
	</li>
</ul>

<h4>3. Move cards by clicking one to move, and then a destination.</h4>
<p>
	Clicking a card will select it, seen by it rising and shining. Then, click another card or area to
	move it to. If you accidentally selected the card or want to change your mind, click the card again
	to deselect it.
</p>

